#include "Cuboid.h"

using namespace render::shape;

Cuboid::Cuboid(glm::vec3 position, Material mat):
    m_position(position),
    m_material(mat),
    m_size(glm::vec3(1.0f))
{
    if (s_instances == 0) {
        Setup();
    }
    
    s_instances++;
}

Cuboid::Cuboid(glm::vec3 position, Material mat, glm::vec3 size): Cuboid(position, mat) {
    m_size = size;
}

void Cuboid::Draw(Shader& shader) const {
    glm::mat4 model(1.0f);
    model = glm::translate(model, m_position);
    model = glm::scale(model, m_size);
    
    shader.SetValue("model", model);
    shader.SetValue("material.diffuse", m_material.diffuse);
    shader.SetValue("material.specular", m_material.specular);
    shader.SetValue("material.shininnes", m_material.shininess);
    
    glBindVertexArray(s_vao);
    glDrawArrays(GL_TRIANGLES, 0, 36);
}

void Cuboid::Setup() {
    glGenVertexArrays(1, &s_vao);
    glGenBuffers(1, &s_vbo);
    
    glBindVertexArray(s_vao);
    glBindBuffer(GL_ARRAY_BUFFER, s_vbo);
    
    glBufferData(GL_ARRAY_BUFFER, s_vertices.size() * sizeof(Vertex), &s_vertices[0], GL_STATIC_DRAW);
    
    glVertexAttribPointer(0, 3, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)0);
    glEnableVertexAttribArray(0);
    glVertexAttribPointer(1, 3, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)offsetof(Vertex, normal));
    glEnableVertexAttribArray(1);
    glVertexAttribPointer(2, 2, GL_FLOAT, GL_FALSE, sizeof(Vertex), (void*)offsetof(Vertex, texCoords));
    glEnableVertexAttribArray(2);
    
    glBindVertexArray(0);
}

